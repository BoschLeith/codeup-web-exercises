//Sunny Weather IF/ELSE
var isSunny = false;

if (isSunny) {
    alert("Don't forget your sunscreen!");
} else {
    alert("There is no need for sunscreen.");
}

//Traffic Light IF/ELSE
var light = "red"

if (light === "green") {
    alert("GO!")
} else if (light === "yellow") {
    alert("SLOW DOWN!")
} else if (light === "red") {
    alert("STOP!")
} else {
    alert("LIGHT INOPERATIVE")
}